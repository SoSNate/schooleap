-- 7. admin_list_users — רשימה לחיפוש ב-UsersTab
-- ────────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION public.admin_list_users(p_search TEXT DEFAULT NULL)
RETURNS TABLE(
  id                      UUID,
  email                   TEXT,
  role                    TEXT,
  is_approved             BOOLEAN,
  is_admin                BOOLEAN,
  subscription_status     TEXT,
  subscription_expires_at TIMESTAMPTZ,
  classroom_code          TEXT,
  applied_coupon          TEXT,
  created_at              TIMESTAMPTZ
)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NOT public.is_admin_caller() THEN
    RAISE EXCEPTION 'not_authorized';
  END IF;
  RETURN QUERY
    SELECT p.id, p.email, p.role, p.is_approved, p.is_admin,
           p.subscription_status, p.subscription_expires_at,
           p.classroom_code, p.applied_coupon, p.created_at
    FROM   public.profiles p
    WHERE  p_search IS NULL
        OR p.email ILIKE '%' || p_search || '%'
    ORDER  BY p.created_at DESC
    LIMIT  500;
END;
$$;

GRANT EXECUTE ON FUNCTION public.admin_list_users(TEXT) TO authenticated;

